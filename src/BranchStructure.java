public class BranchStructure {
    public static void main(String[] args) {
        int x = 30;

        if (x == 10) {
            System.out.print("Value of x is 10");
        } else if (x == 20) {
            System.out.print("Value of x is 20");
        } else if (x == 30) {
            System.out.print("Value of x is 30");
        } else {
            System.out.print("这是else语句");
        }
    }
}
